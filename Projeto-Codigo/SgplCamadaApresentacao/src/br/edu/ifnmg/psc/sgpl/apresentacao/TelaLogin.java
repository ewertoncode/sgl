    private void btnEfetuarLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEfetuarLoginActionPerformed
        Usuario p = new Usuario(0, txtLogin.getText(), String.valueOf(txtSenha.getPassword()));
        
        List<Usuario> lista = dao.Buscar(p);
        
        if(lista.isEmpty()){
            lblMensagem.setText("Login ou Senha errados!");
        } else {
            
            Aplicacao.setUsuario(lista.get(0));
            
            TelaPrincipal principal = new TelaPrincipal();
            
            principal.setTelalogin(this);
            
            principal.setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_btnEfetuarLoginActionPerformed

